// Function to open the sidebar
function openSidebar() {
    const menu = document.getElementById('menuBar');
    menu.classList.add('open'); // Add the "open" class to make it visible
    
}

// Function to close the sidebar
function closeMenu() {
    const menu = document.getElementById('menuBar');
    menu.classList.remove('open'); // Remove the "open" class to hide it
    
}
